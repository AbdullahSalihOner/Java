package Loops;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("Hello World");
            i++;
        }

        int k = 1;
        do {
            System.out.println( "Hello World" );
            k += 1;
        } while (k<10);;
    }
}
